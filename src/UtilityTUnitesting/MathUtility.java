package UtilityTUnitesting;


/**
 * A library of arithmetic methods 
 * @author Aurora Chen
 */

public class MathUtility{

    /**
     * A method to compute the sum of two intergers 
     * @param num1 the first interger 
     * @param num2 the second interger
     * @return the sum of the two intergers
     */
    public static int add(int num1, int num2){
        return num1 + num2;
    }

    /**
     * A method to compute the difference of two intergers
     * @param num1 the first interger 
     * @param num2 the second interger
     * @return the difference of the two intergers
     */
    public static int subtract(int num1, int num2){
        return num1 - num2;
    }

    /**
     * A method to compute the product of two intergers 
     * @param num1 the first interger 
     * @param num2 the second interger
     * @return the product of the two intergers
     */
    public static int multiply(int num1, int num2){
        return num1 * num2;
    }
    

}